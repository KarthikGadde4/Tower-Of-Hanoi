/**
 * This class contains the java enumumeration of positions that the towers can
 * be located in
 *
 * @author karth
 * @version Oct 22, 2024
 */
public enum Position
{
    /**
     * Left tower position
     */
    LEFT,
    /**
     * Center tower position
     */
    CENTER,
    /**
     * Right tower position
     */
    RIGHT,
    /**
     * Default tower position
     */
    DEFAULT;

}
